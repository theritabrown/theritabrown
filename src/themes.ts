export function getTheme(slug?: string) {
  return themes.find((theme) => theme.slug === slug) ?? themes[0]
}

export function themeStyle(slug?: string) {
  const theme = getTheme(slug)

  return {
    '--ink': theme.colors.ink,
    '--muted': theme.colors.muted,
    '--paper': theme.colors.paper,
    '--panel': theme.colors.panel,
    '--panel-strong': theme.colors.panelStrong,
    '--line': theme.colors.line,
    '--accent': theme.colors.accent,
    '--accent-2': theme.colors.accent2,
    '--accent-3': theme.colors.accent3,
    '--feature-bg': theme.colors.featureBg,
    '--feature-ink': theme.colors.featureInk,
    '--button-bg': theme.colors.buttonBg,
    '--button-ink': theme.colors.buttonInk,
    '--theme-shadow': theme.colors.shadow,
    '--body-bg': theme.colors.bodyBg,
    '--card-bg': theme.colors.cardBg,
    '--storefront-bg': theme.colors.storefrontBg,
  } as CSSProperties
}
